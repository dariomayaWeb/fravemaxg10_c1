    private void armarCabecera() {
        ArrayList<Object> columns = new ArrayList<Object>();
        columns.add("ID");
        columns.add("Nombre");
        columns.add("CategorÃ­a");
        columns.add("Cantidad");
        columns.add("Precio");
        for (Object it : columns) {
            modelo.addColumn(it);
        }
        tablaDetallesVenta.setModel(modelo);        
    }
    
    public void borrarFilasTabla(){
        int a = modelo.getRowCount()-1;
        for (int i = a; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }
    
    public void cargarDatos(int idVenta) {
        borrarFilasTabla();
        //venta = ventaData.buscarVenta(idVenta);
        listaDetallesVenta = (ArrayList)detVentData.listaDetalleVentas(idVenta);        
        txtFecha.setText(venta.getFecha().toString());
        txtIdVenta.setText(venta.getIdVenta()+"");
        txtCliente.setText(venta.getCliente().getApellido()+" "+venta.getCliente().getNombre());
        txtTotal.setText(detVentData.montoTotal(venta.getIdVenta())+"");
        for (DetalleVenta detalleVenta : listaDetallesVenta) {
            modelo.addRow(new Object[]{detalleVenta.getIdDetalleVenta(), detalleVenta.getProducto().getNombre(), detalleVenta.getProducto().getCategoria(), detalleVenta.getCantidad(), detalleVenta.getPrecioVenta()});
        }

    }
    
    
